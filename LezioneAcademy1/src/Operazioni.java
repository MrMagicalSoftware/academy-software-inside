
public class Operazioni {

	public static void main(String[] args) {
		
		
		
		int numero1 , numero2 , ris;
		
		
		numero1 = 90;
		numero2 = 7;
		
		
		ris = numero1 + numero2;
		
		System.out.println("La somma vale " + ris);
		
		
		ris = numero1 * numero2;
		
		System.out.println("La moltipli vale " + ris);
		

		ris = numero1 / numero2;
		
		System.out.println("La div vale " + ris );
		
		
		ris = numero1 - numero2;
		
		
		System.out.println("la diff vale " + ris);
		
		
	}

}
