
public class UReversu {

	
	public static void main(String[] args) {
		
		
		
		int[] vett = { 10 , 20 , 30 , 40, 70 , 100 };
		
		// stampare il vettore al contrario
		
		// 40 30 20 10 usare un ciclo
		
		
		
		int i = vett.length - 1 ;
		
		while(i >= 0 ) {
			System.out.print(vett[i] + " ");
			i--;
		}
		
		
	}
	
	
}
